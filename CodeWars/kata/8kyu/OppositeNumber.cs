namespace CodeWars.kata._8kyu
{
    /// <summary>
    /// Very simple, given a number, find its opposite.
    /// 1: -1
    /// 14: -14
    /// -34: 34
    /// </summary>
    class OppositeNumber
    {
        public static int Opposite(int number)
        {
            return number * -1;
        }
    }
}
