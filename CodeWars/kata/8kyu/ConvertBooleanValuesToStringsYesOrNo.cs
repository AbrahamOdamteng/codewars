namespace CodeWars.kata._8kyu
{
    /// <summary>
    /// Given: a boolean value Return: a 'Yes' string for true and a 'No' string for false
    /// </summary>
    class ConvertBooleanValuesToStringsYesOrNo
    {
        public static string boolToWord(bool word)
        {
            return word ? "Yes" : "No";
        }
    }
}
