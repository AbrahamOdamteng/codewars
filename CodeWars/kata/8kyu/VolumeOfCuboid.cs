namespace CodeWars.kata._8kyu
{
    /// <summary>
    /// Bob needs a fast way to calculate the volume of a cuboid with three 
    /// values: length, width and the height of the cuboid. 
    /// Write a function to help Bob with this calculation
    /// </summary>
    class VolumeOfCuboid
    {
        public static double getVolumeOfCubiod(double length, double width, double height)
        {
            return length * width * height;
        }
    }
}
